<p><b>PKM</b> merupakan salah satu upaya yang dilakukan oleh Direktorat Riset dan Pengabdian
Masyarakat (semula Direktorat Penelitian dan Pengabdian kepada Masyarakat Ditjen Dikti),
Direktorat Jenderal Penguatan Riset dan Pengembangan, Kementerian Ristek Dikti untuk
