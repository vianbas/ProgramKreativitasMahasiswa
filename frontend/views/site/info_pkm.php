Pada awalnya, dikenal lima jenis kegiatan yang ditawarkan dalam PKM, yaitu PKMPenelitian (PKM-P), PKM-Kewirausahaan (PKM-K), PKM-Pengabdian kepada Masyarakat
(PKM-M), PKM-Penerapan Teknologi (PKM-T) dan PKM-Penulisan Ilmiah (PKM-I). Sejak
Januari 2009, Ditlitabmas mengelola 6 (enam) PKM. Kompetisi Karya Tulis Mahasiswa
(KKTM) yang semula menjadi tugas Direktorat Akademik dalam pengelolaannya,
dilimpahkan kepada Ditlitabmas. Karena sifatnya yang identik dengan PKM-I, KKTM
selanjutnya dikelola bersama-sama PKM-I dalam PKM-Karya Tulis (PKM-KT). Dengan
demikian, di dalam PKM-KT terkandung dua program penulisan, yaitu: PKM-Artikel Ilmiah
(PKM-AI) dan PKM-Gagasan Tertulis (PKM-GT). PKM-I atau selanjutnya disebut PKM-AI
yang merupakan artikel hasil kegiatan, tidak lagi ditampilkan dalam PIMNAS, namun
dimuarakan pada e-journal. Sedangkan PKM-GT yang berpeluang didiskusikan dalam
forum terbuka, diposisikan sebagai pengganti PKM-AI di PIMNAS.
